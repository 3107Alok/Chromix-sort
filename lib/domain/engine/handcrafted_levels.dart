  /// Main retrieval method. Returns a list of Bottles if the level is handcrafted (1 to 50),
  /// otherwise returns null (meaning it should be procedurally generated).
  static List<Bottle>? getLevel(int levelNumber) {
    if (levelNumber < 1 || levelNumber > 50) return null;
    
    final raw = _levels[levelNumber];
    if (raw == null) return null;

    final bottles = <Bottle>[];
    for (int i = 0; i < raw.length; i++) {
      bottles.add(Bottle(id: i, layers: List<int>.from(raw[i])));
    }
    return bottles;
  }

